package dash.templatemanagement.domain;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;

@Entity
@Table(name = "template")
public class Template {

	@Id
	@GeneratedValue(strategy = GenerationType.SEQUENCE, generator = "template_auto_gen")
	@SequenceGenerator(name = "template_auto_gen", sequenceName = "template_id_seq")
	@Column(name = "id")
	private Long id;

	@Column(name = "name")
	private String name;

	@Column(name = "description")
	private String description;

	@Column(name = "content", length = 30000)
	private String content;

	@Column(name = "deactivated", nullable = false)
	private boolean deactivated;

	public Template() {

	}

	public Long getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public boolean isDeactivated() {
		return deactivated;
	}

	public void setDeactivated(boolean deactivated) {
		this.deactivated = deactivated;
	}

	@Override
	public String toString() {
		return "Template [id=" + id + ", name=" + name + ", description=" + description + ", content=" + content + ", deactivated=" + deactivated + "]";
	}

}
